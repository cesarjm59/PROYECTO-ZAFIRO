class Map:
    def __init__(self, game, seleccion_mapa='1'):
        self.game = game
        self.seleccion_mapa = seleccion_mapa

        if self.seleccion_mapa == '1':
            self.mini_map = mapa_1
        elif self.seleccion_mapa == '2':
            self.mini_map = mapa_2
        elif self.seleccion_mapa == '3':
            self.mini_map = mapa_3

        self.world_map = {}
        self.rows = len(self.mini_map)
        self.cols = len(self.mini_map[0])
        self.get_map()
    def get_map(self):
        for j, row in enumerate(self.mini_map):
            for i, value in enumerate(row):
                if value:
                    self.world_map[(i, j)] = value
    def draw(self):
        for pos in self.world_map:
            pg.draw.rect(self.game.screen, 'darkgray', 
                         (pos[0] * 100, pos[1] * 100, 100, 100), 2)